package day15_ForLoop;

public class ForLoopPractice {
    public static void main(String[] args) {

        for (int i = 1; i<=100; i += 2){

           System.out.print(i+" ");

        }
        System.out.println(" =-=-=-=-=-");

        for (int c = 2; c<=100; c += 2){
            System.out.print(c + " ");
        }

    }
}
